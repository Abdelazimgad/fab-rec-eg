async function signInWithEmail(event) {
  event.preventDefault();
  if (!auth) {
    showUserError('Authentication setup is not available. Check Firebase configuration.');
    return;
  }
  const email = document.getElementById('signin-email').value.trim();
  const password = document.getElementById('signin-password').value;
  try {
    const userCredential = await auth.signInWithEmailAndPassword(email, password);
    await handleAuthenticatedUser(userCredential.user);
    closeAuthModal();
  } catch (error) {
    showUserError(error.message || 'Unable to sign in');
  }
}

async function signUpWithEmail(event) {
  event.preventDefault();
  if (!auth) {
    showUserError('Authentication setup is not available. Check Firebase configuration.');
    return;
  }
  const fullName = document.getElementById('signup-fullname').value.trim();
  const email = document.getElementById('signup-email').value.trim();
  const password = document.getElementById('signup-password').value;

  if (!fullName) {
    showUserError('Full Name is required.');
    return;
  }

  try {
    const userCredential = await auth.createUserWithEmailAndPassword(email, password);
    await userCredential.user.updateProfile({ displayName: fullName });
    currentUserDoc = await saveUserDocument(userCredential.user, { fullName, provider: 'password' });
    setAuthUIState(currentUserDoc);
    closeAuthModal();
  } catch (error) {
    showUserError(error.message || 'Unable to create account');
  }
}

async function signInWithGoogle() {
  if (!firebaseReady) {
    showUserError('Firebase configuration is not set. Please update script.js with your Firebase credentials.');
    return;
  }

  const provider = new firebase.auth.GoogleAuthProvider();
  try {
    const result = await auth.signInWithPopup(provider);
    const user = result.user;
    if (!user.displayName) {
      await showCompleteProfileModal();
      return;
    }
    await handleAuthenticatedUser(user);
    closeAuthModal();
  } catch (error) {
    showUserError(error.message || 'Google sign in failed');
  }
}

function configurePhoneAuth() {
  if (!window.recaptchaVerifier) {
    window.recaptchaVerifier = new firebase.auth.RecaptchaVerifier('recaptcha-container', {
      size: 'invisible',
      callback: () => {}
    });
  }
}

async function sendPhoneOtp(action) {
  if (!firebaseReady) {
    showUserError('Firebase configuration is not set. Please update script.js with your Firebase credentials.');
    return;
  }

  currentPhoneAction = action;
  pendingPhoneFullName = '';
  configurePhoneAuth();
  const phoneNumber = document.getElementById('phone-number-input').value.trim();
  if (!phoneNumber) {
    showUserError('Please enter a valid phone number.');
    return;
  }

  if (currentAuthMode === 'signup') {
    const fullName = document.getElementById('phone-fullname-input').value.trim();
    if (!fullName) {
      showUserError('Full Name is required for phone sign up.');
      return;
    }
    pendingPhoneFullName = fullName;
  }

  try {
    const appVerifier = window.recaptchaVerifier;
    confirmationResult = await auth.signInWithPhoneNumber(phoneNumber, appVerifier);
    document.getElementById('otp-section').classList.remove('hidden');
    document.getElementById('verify-otp-btn').classList.remove('hidden');
  } catch (error) {
    showUserError(error.message || 'OTP send failed');
  }
}

async function verifyPhoneOtp() {
  const code = document.getElementById('otp-code-input').value.trim();
  if (!code) {
    showUserError('Please enter the OTP code.');
    return;
  }

  try {
    const result = await confirmationResult.confirm(code);
    const user = result.user;
    if (currentAuthMode === 'signup' && pendingPhoneFullName) {
      await user.updateProfile({ displayName: pendingPhoneFullName });
    }
    await handleAuthenticatedUser(user);
    closeAuthModal();
    pendingPhoneFullName = '';
  } catch (error) {
    showUserError(error.message || 'OTP verification failed');
  }
}

