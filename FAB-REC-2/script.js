let globalProducts = [];
let currentFilter = 'all';

function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'ar' : 'en';
  document.documentElement.lang = currentLang;
  document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
  document.getElementById('current-lang').textContent = currentLang === 'en' ? 'AR' : 'EN';

  applyTranslations();
  renderProducts(globalProducts); // Re-render to update dynamic translations
  renderCart();
}

function applyTranslations() {
  const dict = i18n[currentLang];
  for (const [key, text] of Object.entries(dict)) {
    const elements = document.querySelectorAll(`.${key}`);
    elements.forEach(el => {
      // Inputs/Placeholders vs innerText
      if (el.tagName === 'INPUT' || el.tagName === 'SELECT' || el.tagName === 'OPTION') {
        if (el.tagName === 'OPTION') {
          el.textContent = text;
        } else {
          el.placeholder = text;
        }
      } else {
        el.textContent = text;
      }
    });
  }
}

document.getElementById('lang-toggle').addEventListener('click', toggleLanguage);

/* Products & Cart Setup */
let cart = JSON.parse(localStorage.getItem('fabrec_cart')) || [];

function saveCart() {
  localStorage.setItem('fabrec_cart', JSON.stringify(cart));
  updateCartUI();
}

function addToCart(productId) {
  const product = globalProducts.find(p => p.id === productId);
  if (!product) return;

  const existing = cart.find(item => item.id === productId);
  if (existing) {
    existing.quantity += 1;
  } else {
    cart.push({ ...product, quantity: 1 });
  }
  saveCart();

  // Open cart panel
  openCart();
}

function removeFromCart(productId) {
  cart = cart.filter(item => item.id !== productId);
  saveCart();
}

function updateQuantity(productId, change) {
  const item = cart.find(item => item.id === productId);
  if (item) {
    item.quantity += change;
    if (item.quantity <= 0) {
      removeFromCart(productId);
    } else {
      saveCart();
    }
  }
}

function updateCartUI() {
  const countEl = document.getElementById('cart-count');
  const itemsContainer = document.getElementById('cart-items');
  const priceEl = document.getElementById('cart-total-price');

  const totalCount = cart.reduce((sum, item) => sum + item.quantity, 0);
  const totalPrice = cart.reduce((sum, item) => sum + (item.price * item.quantity), 0);

  countEl.textContent = totalCount;
  priceEl.textContent = `${totalPrice} ${i18n[currentLang]['t-currency']}`;

  renderCart();
}

function renderCart() {
  const itemsContainer = document.getElementById('cart-items');
  itemsContainer.innerHTML = '';

  if (cart.length === 0) {
    itemsContainer.innerHTML = `<p style="text-align:center; padding: 2rem; color:var(--text-gray)">Empty</p>`;
    return;
  }

  cart.forEach(item => {
    const itemName = item.name[currentLang] || item.name['en'];
    const el = document.createElement('div');
    el.className = 'cart-item';
    el.innerHTML = `
      <img src="${item.image}" alt="">
      <div class="cart-item-info">
        <div class="cart-item-title">${itemName}</div>
        <div class="cart-item-price">${item.price} ${i18n[currentLang]['t-currency']}</div>
        <div class="cart-controls">
          <button onclick="updateQuantity(${item.id}, -1)">-</button>
          <span>${item.quantity}</span>
          <button onclick="updateQuantity(${item.id}, 1)">+</button>
          <button class="remove-item" onclick="removeFromCart(${item.id})"><i class="fa-solid fa-trash"></i></button>
        </div>
      </div>
    `;
    itemsContainer.appendChild(el);
  });
}

function renderProducts(products) {
  globalProducts = products;
  const grid = document.getElementById('product-grid');
  grid.innerHTML = '';

  const filteredProducts = currentFilter === 'all'
    ? products
    : products.filter(p => p.category === currentFilter);

  filteredProducts.forEach(product => {
    const name = product.name[currentLang] || product.name['en'];
    const desc = product.description[currentLang] || product.description['en'];
    const btnText = i18n[currentLang]['t-add-to-cart'];
    const viewText = i18n[currentLang]['t-view-details'] || 'View Details';
    const handmadeText = i18n[currentLang]['t-pdm-handmade'] || '🇪🇬 Egyptian Handmade';

    const card = document.createElement('div');
    card.className = 'product-card';
    card.innerHTML = `
      <div class="product-img-wrap" onclick="openProductModal(${product.id})">
        <img src="${product.image}" class="product-img" alt="${name}">
        <div class="product-view-overlay">
          <span><i class="fa-solid fa-eye"></i> ${viewText}</span>
        </div>
      </div>
      <div class="product-info">
        <div class="product-handmade-tag"><i class="fa-solid fa-hands"></i> ${handmadeText}</div>
        <h3 class="product-title" onclick="openProductModal(${product.id})" style="cursor:pointer">${name}</h3>
        <p class="product-desc">${desc}</p>
        <div class="product-footer">
          <span class="product-price">${product.price} ${i18n[currentLang]['t-currency']}</span>
          <button class="btn btn-primary" onclick="addToCart(${product.id})">
            <i class="fa-solid fa-plus"></i> ${btnText}
          </button>
        </div>
      </div>
    `;
    grid.appendChild(card);
  });
}

async function loadProducts() {
  try {
    const res = await fetch('products.json');
    if (!res.ok) throw new Error('Failed to load');
    const products = await res.json();
    renderProducts(products);
  } catch (err) {
    console.warn('Using fallback products due to fetch error (likely CORS/local):', err);
    renderProducts(fallbackProducts);
  }
}

