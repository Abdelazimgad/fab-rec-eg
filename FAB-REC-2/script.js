async function saveUserDocument(user, extra = {}) {
  if (!db) return null;
  const userRef = db.collection('users').doc(user.uid);
  const snapshot = await userRef.get();
  const now = firebase.firestore.FieldValue.serverTimestamp();

  const payload = {
    uid: user.uid,
    fullName: extra.fullName || user.displayName || '',
    email: user.email || '',
    phone: user.phoneNumber || extra.phone || '',
    provider: extra.provider || (user.providerData[0] ? user.providerData[0].providerId : 'unknown'),
    photoURL: user.photoURL || '',
    lastLogin: now,
  };

  if (!snapshot.exists) {
    await userRef.set({
      ...payload,
      createdAt: now,
    });
  } else {
    await userRef.update(payload);
  }

  const doc = await userRef.get();
  return { id: doc.id, ...doc.data() };
}

async function handleAuthenticatedUser(user) {
  if (!user) return;
  currentUser = user;

  let userDoc = null;
  if (db) {
    try {
      const docRef = await db.collection('users').doc(user.uid).get();
      if (docRef.exists) {
        userDoc = { id: docRef.id, ...docRef.data() };
      }
    } catch (error) {
      console.error('Failed to fetch user doc', error);
    }
  }

  const fullName = user.displayName || (userDoc ? userDoc.fullName : '');
  if (!fullName) {
    showCompleteProfileModal();
    return;
  }

  currentUserDoc = db ? await saveUserDocument(user, { fullName, provider: user.providerData[0]?.providerId }) : {
    uid: user.uid,
    fullName,
    email: user.email || '',
    phone: user.phoneNumber || '',
    provider: user.providerData[0]?.providerId || 'unknown',
    photoURL: user.photoURL || '',
    createdAt: null,
    lastLogin: null
  };
  setAuthUIState(currentUserDoc);
}

